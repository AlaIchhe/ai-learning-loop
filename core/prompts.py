# 全局汇总提示
# =============================================================================

FINAL_SUMMARY_PROMPT = """\
你是一位学术辩论裁判。整场辩论已经结束。请根据所有轮次的辩论记录，撰写一份最终总结报告。

报告应包含：
1. 辩论主题回顾
2. 各轮次要点概述
3. 双方整体表现评价
4. 最终胜负判定及理由
5. 辩论中的关键转折点

控制在 500 字以内，语言客观中立。
"""


def final_summary_prompt(topic: str, history_json: str) -> str:
    """生成最终总结的用户提示。

    Args:
        topic: 辩论主题。
        history_json: 所有轮次的 JSON 序列化记录。
    """
    return (
        f"辩论主题：{topic}\n\n"
        f"所有轮次记录：\n{history_json}\n\n"
        f"请撰写最终总结报告。"
    )
